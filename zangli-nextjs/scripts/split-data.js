const startYear = 1951;
const dataDir = path.join(__dirname, '../src/data');

// 创建数据目录
if (!fs.existsSync(dataDir)) {
  fs.mkdirSync(dataDir, { recursive: true });
}

// 生成年度数据文件
specialDays.forEach((yearData, index) => {
  const year = startYear + index;
  const yearFile = {
    year: year,
    data: yearData,
    metadata: {
      months: yearData.length,
      startDate: year === 1951 ? '1951-01-08' : `${year}-01-01`,
      endDate: year === 2050 ? '2051-02-11' : `${year}-12-31`
    }
  };
  
  const filename = `${year}.json`;
  const filepath = path.join(dataDir, filename);
  
  fs.writeFileSync(filepath, JSON.stringify(yearFile, null, 2));
  console.log(`Generated ${filename}`);
});

// 生成十年数据文件
for (let decade = 1950; decade < 2050; decade += 10) {
  const decadeData = [];
  const startIndex = Math.max(0, decade - startYear);
  const endIndex = Math.min(specialDays.length, decade + 10 - startYear);
  
  for (let i = startIndex; i < endIndex; i++) {
    if (specialDays[i]) {
      decadeData.push({
        year: startYear + i,
        data: specialDays[i]
      });
    }
  }
  
  if (decadeData.length > 0) {
    const decadeFile = {
      decade: decade,
      years: decadeData,
      metadata: {
        startYear: decadeData[0].year,
        endYear: decadeData[decadeData.length - 1].year,
        totalYears: decadeData.length
      }
    };
    
    const filename = `${decade}s.json`;
    const filepath = path.join(dataDir, filename);
    
    fs.writeFileSync(filepath, JSON.stringify(decadeFile, null, 2));
    console.log(`Generated ${filename}`);
  }
}

// 生成索引文件
const indexFile = {
  startYear: 1951,
  endYear: 2050,
  totalYears: specialDays.length,
  startDate: '1951-01-08',
  endDate: '2051-02-11',
  dataFormat: {
    description: 'Tibetan calendar data with special days',
    structure: {
      year: 'YYYY format',
      data: 'Array of months, each month is array of special days',
      specialDays: {
        negative: 'Missing day (缺日)',
        positive: 'Leap day (闰日)', 
        zero: 'Leap month (闰月)',
        empty: 'Normal month (吉祥月)'
      }
    }
  },
  files: {
    annual: `${startYear}.json to ${startYear + specialDays.length - 1}.json`,
    decades: ['1950s.json', '1960s.json', '1970s.json', '1980s.json', '1990s.json', '2000s.json', '2010s.json', '2020s.json', '2030s.json', '2040s.json']
  }
};

fs.writeFileSync(path.join(dataDir, 'index.json'), JSON.stringify(indexFile, null, 2));
console.log('Generated index.json');

